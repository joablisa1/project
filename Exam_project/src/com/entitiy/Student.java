package com.entitiy;

public class Student {
	private String firstName;
	private String lastName;
	private String date;
	private String dateofBirth;
	private int adm_number;
	private String address;
	private String gender;
	private String parent_name;
	private String phone_Number;
	private String enrolled_class;
	
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getDateofBirth() {
		return dateofBirth;
	}
	public void setDateofBirth(String dateofBirth) {
		this.dateofBirth = dateofBirth;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public int getAdm_number() {
		return adm_number;
	}
	public void setAdm_number(int adm_number) {
		this.adm_number = adm_number;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getParent_name() {
		return parent_name;
	}
	public void setParent_name(String parent_name) {
		this.parent_name = parent_name;
	}
	public String getPhone_Number() {
		return phone_Number;
	}
	public void setPhone_Number(String phone_Number) {
		this.phone_Number = phone_Number;
	}
	public String getEnrolled_class() {
		return enrolled_class;
	}
	public void setEnrolled_class(String enrolled_class) {
		this.enrolled_class = enrolled_class;
	}
	
	

}
